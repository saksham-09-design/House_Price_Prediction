import os
import pandas as pd
import numpy as np
import joblib

from sklearn.model_selection import StratifiedShuffleSplit
from sklearn.pipeline import Pipeline
from sklearn.compose import ColumnTransformer
from sklearn.impute import SimpleImputer
from sklearn.preprocessing import StandardScaler, OneHotEncoder
from sklearn.ensemble import RandomForestRegressor
from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score

MODEL_FILE = "model.pkl"
PIPELINE_FILE = "pipeline.pkl"

def build_pipeline(num_attribs, cat_attribs):
    num_pipeline = Pipeline([
        ("imputer", SimpleImputer(strategy="median")),
        ("scaler", StandardScaler())
    ])
    cat_pipeline = Pipeline([
        ("onehot", OneHotEncoder(handle_unknown="ignore"))
    ])
    full_pipeline = ColumnTransformer([
        ("num", num_pipeline, num_attribs),
        ("cat", cat_pipeline, cat_attribs)
    ])
    return full_pipeline

# Load dataset
housing = pd.read_csv("housing.csv")
housing['income_cat'] = pd.cut(
    housing["median_income"], 
    bins=[0.0, 1.5, 3.0, 4.5, 6.0, np.inf], 
    labels=[1, 2, 3, 4, 5]
)

# Stratified split into train and test
split = StratifiedShuffleSplit(n_splits=1, test_size=0.1, random_state=42)
for train_index, test_index in split.split(housing, housing['income_cat']):
    strat_train_set = housing.loc[train_index].drop("income_cat", axis=1)
    strat_test_set = housing.loc[test_index].drop("income_cat", axis=1)

train_data = strat_train_set
test_data = strat_test_set

train_labels = train_data["median_house_value"].copy()
train_features = train_data.drop("median_house_value", axis=1)

test_labels = test_data["median_house_value"].copy()
test_features = test_data.drop("median_house_value", axis=1)

num_attribs = train_features.drop("ocean_proximity", axis=1).columns.tolist()
cat_attribs = ["ocean_proximity"]

if not os.path.exists(MODEL_FILE):
    # Train model
    pipeline = build_pipeline(num_attribs, cat_attribs)
    train_prepared = pipeline.fit_transform(train_features)

    model = RandomForestRegressor(random_state=42)
    model.fit(train_prepared, train_labels)

    # Save model and pipeline
    joblib.dump(model, MODEL_FILE)
    joblib.dump(pipeline, PIPELINE_FILE)
    print("Model trained and saved.")

# Load model and evaluate accuracy
model = joblib.load(MODEL_FILE)
pipeline = joblib.load(PIPELINE_FILE)

test_prepared = pipeline.transform(test_features)
predictions = model.predict(test_prepared)

mae = mean_absolute_error(test_labels, predictions)
rmse = np.sqrt(mean_squared_error(test_labels, predictions))
r2 = r2_score(test_labels, predictions)

print("\nModel Evaluation Results:")
print(f"Mean Absolute Error (MAE): {mae:.2f}")
print(f"Root Mean Squared Error (RMSE): {rmse:.2f}")
print(f"R2 Score: {r2:.4f}")
